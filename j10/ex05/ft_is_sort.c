int ft_is_sort(int *tab, int length, int (*f)(int, int))
{
	int i;
	int a;

	i = 0;
	a = f(tab[i], tab[i + 1]);
	while (i < length - 1)
	{
		if ((a >= 0 && f(tab[i], tab[i + 1]) >= 0) ||
				(a <= 0 && f(tab[i], tab[i + 1] <= 0)))
		{
			a = f(tab[i], tab[i + 1]);
			i++;
		}
		else
			return (0);
	}
	return (1);
}
