#include <stdlib.h>

char *ft_strdup(char *src)
{
	int		i;
	char	*p;

	i = 0;
	while (src[i++] != '\0')
		;
	p = malloc((i + 1) * sizeof(char));
	i = 0;
	while (src[i] != '\0')
	{
		p[i] = src[i];
		i++;
	}
	p[i + 1] = '\0';
	return (p);
}
