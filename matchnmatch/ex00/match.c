int match(char *s1, char *s2)
{
	if (*s2)
		if (*s2 == '*' && *(s2 + 1) == '\0')
			return (1);
	if (*s2 == '*' && *s1)
	{
		if (match(s1, s2 + 1))
			return (1);
		else
			return (match(s1 + 1, s2));
	}
	if (*s2 == '\0' && *s1 == '\0')
		return (1);
	if (*s1 == *s2)
		return (match(s1 + 1, s2 + 1));
	else
		return (0);
	return (2);
}
