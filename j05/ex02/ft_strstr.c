char *ft_strstr(char *str, char *to_find)
{
	int i;
	int n;

	n = 0;
	i = 0;
	while (str[n] != '\0')
	{
		while (str[n + i] == to_find[i])
		{
			i++;
			if (to_find[i] == '\0')
				return (&str[n]);
		}
		n++;
		i = 0;
	}
	return (0);
}
