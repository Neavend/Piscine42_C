int	ft_sqrt(int nb)
{
	int n;

	n = 1;
	if (nb <= 0)
		return (0);
	else
	{
		if (nb == 1)
			return (1);
		else
		{
			while (n <= nb / 2)
			{
				if (n * n == nb)
					return (n);
				else
				{
					n++;
				}
			}
		}
		return (0);
	}
	return (0);
}
