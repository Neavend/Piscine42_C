int	ft_is_prime(int nb)
{
	int n;

	n = 2;
	if (nb <= 0)
		return (0);
	else
	{
		while (n <= nb / 1)
		{
			if (nb % n == 0)
				return (0);
			else
			{
				n++;
			}
		}
		return (1);
	}
}
